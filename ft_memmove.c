#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dst;
	if (!d && !s)
		return (0);
	if (s < d)
	{
		s += len;
		d += len;
		while (len--)
			*(char *)--d = *(char *)--s;
	}
	else
		while (len--)
			*(char *)d++ = *(char *)s++;
	return (dst);
}
